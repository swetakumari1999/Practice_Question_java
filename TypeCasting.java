public class TypeCasting {
    public static void main(String args[])
    {
        // byte b =125;
        int a = 257;
        byte k = (byte) a;

        float f = 5.6f;
        int t = (int) f;

        System.out.println(k);
        System.out.println(t);


        byte c =10;
        byte b = 30;
        int result = (int)c*b;

        System.out.println(result);

    }
}
